        logger.info(f"üñ®Ô∏è –£—Å—Ç—Ä–æ–π—Å—Ç–≤–æ —Å–∫–∞–Ω–µ—Ä–∞: {config.SCANNER_DEVICE or '–∞–≤—Ç–æ–æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ'}")
        logger.info(f"üë• –ê–≤—Ç–æ—Ä–∏–∑–æ–≤–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏: {len(config.TELEGRAM_CHAT_IDS)}")
        
        # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –∏ –∑–∞–ø—É—Å–∫ –±–æ—Ç–∞
